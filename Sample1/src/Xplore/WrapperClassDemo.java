package Xplore;

public class WrapperClassDemo {

	public static void main(String[] args) {
		//-----------Autoboxing-----------------//
		
		int a=47;
		Integer a1 = new Integer(a);
		System.out.println(a1);
	
		
		//-----------unboxing--------------//
		 
		int unboxing = a1;
		System.out.println(unboxing);
		
		System.out.println("-------------------------");
		System.out.println();
		
		
	}

}
