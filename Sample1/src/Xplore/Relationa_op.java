package Xplore;

public class Relationa_op {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		System.out.println("(Num1 > Num2) : " +(num1>num2));
		System.out.println("(Num1 < Num2) : " +(num1<num2));
		System.out.println("(Num1 >= Num2): " +(num1>=num2));
		System.out.println("(Num1 =< Num2): " +(num1<=num2));
		System.out.println("(Num1 != Num2): " +(num1!=num2));
		System.out.println("(Num1 == Num2): " +(num1 == num2));

	}

}
